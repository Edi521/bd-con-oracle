 SI UNA TRANSACCION DE QUEDA SIN CONFIRMAR LA DB QUEDARA BLOQUEADA PPOR EL USUARIO QUE REALIZO LA SENTENCIA Y NINGUN OTRO PODRA REALIZAR OPERACIONES
 DE PERSISTENCIA EN LA BD*/
 

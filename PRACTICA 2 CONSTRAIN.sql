 --CONSTRAINT: SON RESTRICCIONES QUE SE APLICAN A UNA ENTIDAD PARA GARANTIZAR LA INTEGRIDAD, CONSISTENCIA Y LA VALIDEZ DE LOS DATOS ALMACENADOS
 --DDL CON PRYMARY KEY ---> PRIMERA FORMA
 CREATE TABLE EMPLEADO(
    ID_EMPLEADO NUMBER,
    NOMBRE NVARCHAR2(100),
    AP NVARCHAR2(100),
    EDAD NUMBER,
    PUESTO NVARCHAR2(100),
    DEPARTAMENTO NVARCHAR2(100),
    PRIMARY KEY(ID_EMPLEADO)
 );
 
 -- SEGUNDA FORMA
 CREATE TABLE EMPLEADO(
    ID_EMPLEADO NUMBER PRIMARY KEY,
    NOMBRE NVARCHAR2(100)
 );
 
 --TERCER FORMA ---------> CONTSRAINT + NOMBRE_CONSTRAINT(TABLA+PK)+PRIMARY KEY + CAMPO QUE SERA LA CLAVE
 CREATE TABLE EMPLEADO(
    ID_EMPLEADO NUMBER,
    NOMBRE NVARCHAR2(100),
    AP NVARCHAR2(100),
    EDAD NUMBER,
    PUESTO NVARCHAR2(100),
    DEPARTAMENTO NVARCHAR2(100),
    CONSTRAINT EMPLEADO_PK PRIMARY KEY(ID_EMPLEADO)
 );
 
 SELECT * FROM EMPLEADO;
 

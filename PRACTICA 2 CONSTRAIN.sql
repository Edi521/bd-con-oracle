/*
3 tipos de comandos:

DDL: Lenguaje de definicion de datos -> comandos utilizadas para estructurar datos: CREATE, DROP, TRUNCATE Y ALTER
DML: Lenguaje de manipulacion de datos -> comandos que se utuliza para manipular los datos: INSERT, UPDATE, DELETE Y SELECT
DCL: Lenguaje de control de datos -> comandos que sirven para controlar los permisos: GRANT Y REVOKE

*/

